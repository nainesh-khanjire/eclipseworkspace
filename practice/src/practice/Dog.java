
public class Dog {

		String name;
		int age;
		
		
		public Dog() {
			
			
		}
		
		public Dog(String name) { // no return type in constructor
			this.name = name;
		}
		
		public Dog(int age) {
				
			this.age = age;
			
		}
		
		public Dog(String name, int age) {
			this.name = name;
			this.age = age;
			
		}
		
		
}
